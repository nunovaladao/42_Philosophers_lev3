#include "philo.h"

int valid_args(char **av)
{
    int i;
    int j;

    i = 0;
    while (av[++i])
    {
        j = 0;
        while (av[i][++j])
        {
            if (!ft_isdigit(av[i][j]))
                return (0);
        }
    }
    return (1);
}
